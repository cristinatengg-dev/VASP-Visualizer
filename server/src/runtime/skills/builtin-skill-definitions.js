function toPersistedSkillDefinition(skill) {
  const {
    _id,
    skillId,
    version,
    latest,
    requiredArtifacts,
    inputSchemaRef,
    steps,
    validatorIds,
    outputArtifacts,
    approvalPolicy,
    retryPolicy,
    failurePolicy,
    contextPolicy,
    status,
  } = skill;

  return {
    _id,
    skillId,
    version,
    latest,
    requiredArtifacts,
    inputSchemaRef,
    steps,
    validatorIds,
    outputArtifacts,
    approvalPolicy,
    retryPolicy,
    failurePolicy,
    contextPolicy,
    status,
  };
}

function getBuiltinSkillDisplay(skillId, version) {
  return BUILTIN_RUNTIME_SKILLS.find((skill) => skill.skillId === skillId && skill.version === version)?.display || null;
}

